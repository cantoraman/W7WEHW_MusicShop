public abstract class MusicInstrument {

    String brand;
    String type;
    String color;
    double buyPrice;
    double sellPrice;


    public MusicInstrument(String brand, String type, String color, double buyPrice, double sellPrice) {

        this.brand= brand;
        this.type = type;
        this.color = color;
        this.buyPrice = buyPrice;
        this.sellPrice = sellPrice;

    }




}
